export function placeHolder(name) {
  switch (name) {
  case "fullName":
    return "Jon Doe";
  case "password":
    return "Password";
  case "phone":
    return "+1 XXX XXX XXXX";
  case "company":
    return "Company";
  case "companyPosition":
    return "Company Role";
  case "email":
    return "Email";
  case "billingAddress":
    return "Address";
  case "billingState":
    return "State";
  case "billingCity":
    return "City";
  case "billingZipCode":
    return "Zip code";
  case "address":
    return "Address";
  case "state":
    return "State";
  case "city":
    return "City";
  case "zipCode":
    return "Zip code";
  }
}